    private void listaDeClientes() {

        nome = "'%" + txtNome.getText() + "%'";

        try {

            banco.conectar();
            var sql = "SELECT * FROM usuario WHERE Nome LIKE " + nome + " or usuario LIKE " + nome + " or tipo LIKE "+nome+"";

            PreparedStatement stmt = banco.connection.prepareStatement(sql);

            try (var result = stmt.executeQuery()) {
                DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
                modelo.setNumRows(0);

                if (!result.isBeforeFirst()) {
                    JOptionPane.showMessageDialog(null, "NÃ£o Existe Registro no Banco de Dados !");
                } else {
                    while (result.next()) {
                        modelo.addRow(new Object[]{result.getString("id"), result.getString("nome"), result.getString("usuario"),result.getString("tipo")});
                    }

                }

            }
            banco.connection.close();

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro no Banco: " + erro.getMessage());
        }

    }

