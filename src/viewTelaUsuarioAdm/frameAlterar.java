    //Fun√ß√£o para Listar todos os dados do Banco de dados
    public void ListarClientes() {

        //V√°riavel respons√°vel por pegar o nome da informa√ß√£o para filtar no banco de dados
        nomeBusca = "'%" + txtNomeBuscar.getText() + "%'";

        try {
            //Iniciando a Classe do Banco de dados
            banco.conectar();

            //C√≥digo do SQL aonde vai ser executado a fun√ß√£o.
            var sql = "SELECT * FROM cliente WHERE Nome LIKE " + nomeBusca + " or usuario LIKE " + nomeBusca + " ";

            //Preparando o Comando SQL para ser Executado
            PreparedStatement stmt = banco.connection.prepareStatement(sql);

            //Verificando e Executando o Comando SQL
            try (var result = stmt.executeQuery()) {
                //Iniciando as propriedades da Tabela Java
                DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
                modelo.setNumRows(0);

                if (!result.isBeforeFirst()) {
                    JOptionPane.showMessageDialog(null, "N√£o Existe Registro no Banco de Dados !");
                } else {
                    //Vai se repetir um loop at√© zerar o banco de Dados
                    while (result.next()) {
                        //Vai ser adicionado cada dado de cada linha o Banco de dados Possue. com o metodo result buscando as colunas do Banco de Dados.
                        modelo.addRow(new Object[]{result.getString("id"), result.getString("nome"), result.getString("usuario"), result.getString("senha")});

                    }

                }

            }
            //Fecha o banco de dados
            banco.connection.close();

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro no Banco: " + erro.getMessage());
        }

    }

    public void LimparCampos() {
        txtNome.setText("");
        txtUsuario.setText("");
        txtSenha.setText("");

    }

    //Veificando os Campos se est√£o preenchidos
    private void verificarCamposSeEstaoPreechidos() {

        try {
            //Vefificando Se foi Preechido os Campos
            if (cliente.getNome().isEmpty() || cliente.getUsuario().isEmpty() || cliente.getSenha().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha os Campos Corretamente !!");
            } else {
                verificandoCadaCampoSeEstaPreenchido();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }

    }

    private void verificandoCadaCampoSeEstaPreenchido() {
        try {
            //Verificando se Cada Campos foi Preenchido Corretamente
            if (cliente.getNome().length() < 3) {
                JOptionPane.showMessageDialog(null, "Campo Nome Precisa ter no Minino 3 Caracteres");
            } else if (cliente.getUsuario().length() < 5) {
                JOptionPane.showMessageDialog(null, "Campo Usu√°rio Precisa ter no Minino 5 Caracteres");

            } else if (cliente.getSenha().length() < 6) {
                JOptionPane.showMessageDialog(null, "Campo Senha Precisa ter no Minino 6 Caracteres");
            } else {
                cadastrarClienteNoBanco();

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }

    }

    //Alterando os Dados do Banco
    private void cadastrarClienteNoBanco() {

        try {
            //Conectando ao Banco de Dados
            banco.conectar();

            //Verificando se Foi Estabelecida conex√£o com o Banco
            if (banco.estaConectado()) {

                //Iniando as informa√ß√µes da Tabela Java
                int setar = Tabela.getSelectedRow();

                //Alterando o Clienete no Banco de Dados
                //Foi colocado como o ID a primeira Posi√ß√£o do Cliente Selecionado l√° na Tabela Java.
                banco.EditarCliente(Tabela.getModel().getValueAt(setar, 0).toString(), cliente.getNome(), cliente.getUsuario(), cliente.getSenha());
                JOptionPane.showMessageDialog(null, "Clienete Alterado Com Sucesso !!");
                LimparCampos();

                banco.DesconectarBanco();

            } else {
                JOptionPane.showMessageDialog(null, "Banco N√£o Est√° Conectado !!");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            banco.DesconectarBanco();
        } finally {
            banco.DesconectarBanco();
        }

    }

